package JavaBasics;

public class Arithmeticoperator2 {
    private static double PI=3.141;

    public static void main(String[] args) {
        double radius= 5.0;
        double area=PI * radius *radius;
        double circle=2* PI * radius;
        System.out.println("Perimeter of Area is:"+area);
        System.out.println("Perimeter of circle is:"+circle);
    }
}
